//switch case is also very similar to syntax of switch in c

no =window.prompt("enter a no b/w 1 to 5");

switch(no)
{
        case 1: 
        document.write("no is 1");
        break;
        case 2: 
        document.write("no is 2");
        break;
        case 3: 
        document.write("no is 3");
        break;
        case 4: 
        document.write("no is 4");
        break;
        case 5: 
        document.write("no is 5");
        break;
        default:
        document.write("enter no b/w 1 to 5");
        break;


}